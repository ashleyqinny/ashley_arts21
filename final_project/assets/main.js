$(document).ready(function() {
        var $cursor = $("#hello_cursor");
        $cursor.hide();
        $(".hello").mousemove(function(e) {
            $cursor.stop(1, 1).fadeIn();
            $cursor.offset({
                top: e.pageY - $cursor.outerHeight(),
                left: e.pageX - ($cursor.outerWidth()/2)
            });
        }).mouseleave(function() {
            $cursor.fadeOut();
        });
    });

